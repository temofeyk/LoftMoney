package com.temofey.k.android.loftmoney.data.api.model;

public class ItemRemote {

    private String id;
    private String name;
    private Integer price;
    private String type;
    private String date;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Integer getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }

    public String getDate() {
        return date;
    }
}
